export interface OwnershipTransferred0Event {
  previousOwner: string;
  newOwner: string;
}

export interface Transfer0Event {
  from: string;
  to: string;
  tokenId: ethers.BigNumber;
}

export interface EvmEvent {
  data: string;
  topics: string[];
}

